
class StudentData {
	
	int stdId;
	String  stdName;
	int phy_Mrk;
	int chem_Mrk;
	
	public StudentData() {
		super();
	}

	public StudentData(int stdId, String stdName, int phy_Mrk, int chem_Mrk) {
		super();
		this.stdId = stdId;
		this.stdName = stdName;
		this.phy_Mrk = phy_Mrk;
		this.chem_Mrk = chem_Mrk;
	}
    
	
	
	
}
